class ArgvTestCase < Test::Unit::TestCase
  include ExerbTestCase

  def name
    return 'test-argv'
  end

  def test_argv
    ret1 = read_result(@name)
    ret2 = execute_exe(@name, 'a b c d e')
    assert_equal(ret1, ret2)
  end

end # ArgvTestCase

